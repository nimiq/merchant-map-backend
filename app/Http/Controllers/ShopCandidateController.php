     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        // Validate that place_id is set and it has valid currencies
        $request->validate([
            'place_id' => 'required',
            'currencies' => 'required|array|distinct|exists:App\Models\Currency,id'
        ]);

        $shopCandidate = new ShopCandidate($request->all());
        $shopCandidate->resolved = false; // All shopCandidates created via API are not resolved yet
        $shopCandidate->save();

        return response()->json(['message' => "Shop candidate successfully created."], 201);
    }

